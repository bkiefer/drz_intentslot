def flatten(l):
	return [item for sublist in l for item in sublist]


def read_data(path_to_folder):
	# read the text files with annotations one by one
	data_dict = {"train": {}, "dev": {}, "test": {}} # {"train": {"id": {"utterance": str, "slots": {"slot_type": [(tags_as_str, marked_utterance), ...], ...}}}}

	for data_file in [f for f in glob.glob(path_to_folder+"*") if f.endswith(".csv")]: # 
		file_name = data_file.split("/")[-1].strip()[:-4]
		file_name_spl = file_name.split("_")
		if len(file_name_spl) < 4:
			# not partitioned
			continue
		partition = file_name_spl[-1].strip()
		slot_type = file_name_spl[-2].strip()

		df = pd.read_csv(data_file, header=0)

		ids = df["id"]
		utterances = df["tokens"]
		tags_str = df["tags"]

		for _id, _utt, _tags in zip(ids, utterances, tags_str):
			if not _id in data_dict[partition].keys():
				data_dict[partition][_id] = dict()
				data_dict[partition][_id]["utterance"] = _utt
				data_dict[partition][_id]["slots"] = {}
			if not slot_type in data_dict[partition][_id]["slots"].keys():
				data_dict[partition][_id]["slots"][slot_type] = []
			# make target sequence (to be generated by an LLM)
			toks = _utt.split()
			tags = _tags.split()
			try:
				assert(len(toks) == len(tags))
			except AssertionError:
				continue
			target = []
			prev_label = "O"
			for tok, label in zip(toks, tags):

				if label == "O" and prev_label != "O":
					target.append("**")
				elif label != "O" and prev_label == "O":
					target.append("**")
					
				target.append(tok)
				prev_label = label
			if prev_label != "O":
				target.append("**")

			target = " ".join(target)

			data_dict[partition][_id]["slots"][slot_type].append((_tags, target))

	return data_dict


def get_instances(data):

	utterances = []
	ids = []
	slots = []
	marked_utterances = []
	gold_bio = [] # List of lists of separate tags
	for _id, info_dict in data.items():
		utt = info_dict["utterance"]
		slots_dict = info_dict["slots"]
		for slot, l in slots_dict.items():
			for tpl in l:
				utterances.append(utt)
				ids.append(_id)
				slots.append(slot)
				gold_bio.append([t for t in tpl[0].split() if t != ""])
				marked_utterances.append(tpl[1])

	return utterances, ids, slots, gold_bio, marked_utterances


def marked_utterance_to_bio(marked_utterance):
	bio_list = []
	marked_utterance_spl = marked_utterance.strip().split()
	curr_tag = "O"
	for token in marked_utterance_spl:
		if token == "**" and curr_tag == "O":
			curr_tag = "B"
		elif token == "**" and curr_tag != "O":
			curr_tag = "O"
		elif curr_tag == "B":
			bio_list.append(curr_tag)
			curr_tag = "I"
		else:
			bio_list.append(curr_tag)
	try:
		utterance = marked_utterance.replace("**", "")
		toks = [t for t in utterance.split() if t != ""]
		assert(len(toks) == len(bio_list))
	except AssertionError:
		print("AssertionError!!!")
		print(marked_utterance.split())
		print(toks)
		print(bio_list)
		print("*****")
	return bio_list


def identify_slots(model_name, gen_model_name, data_dict):

	test_data = data_dict["test"]
	print("Len test:", len(test_data.keys()))

	# Create a sampling params object.
	sampling_params = SamplingParams(
		temperature=0.7, ### thinking 0.6 / no thinking 0.7 (only DeepSeek / Qwen3 relevant)
		top_p=0.8, ### thinking 0.95 / no thinking 0.8 (only DeepSeek / Qwen3 relevant)
		top_k=20, ### thinking 20 / no thinking 20 (only DeepSeek / Qwen3 relevant)
		min_p=0, ### thinking 0 / no thinking 0 (only DeepSeek / Qwen3 relevant)
		max_tokens=128,
	)

	# Create an LLM
	if torch.cuda.is_bf16_supported():
		dtype = torch.bfloat16
	else:
		dtype = torch.float32

	gen_model = LLM(
		model=gen_model_name,
		dtype=dtype,
		enable_lora=True,
		max_lora_rank=16,
		gpu_memory_utilization=0.9,
		tensor_parallel_size=2,
		max_model_len = 4096,
	)

	# make a prompt
	system = "You are a helpful AI assistant for information extraction."
	instruction = "You are given a radio talk utterance in German and a slot type also in German. There are 5 possible slot types:\n"+\
				"Einheit: a person, a group of people performing a task or an action\n"+\
				"Auftrag: an action or a task someone is to do as part of the mission, as well as a completed or ongoing task (action); often a verb or a noun phrase\n"+\
				"Mittel: a robot, a device, or something else used to accomplish the given task\n"+\
				"Ziel: a location (a destination) someone is to get to\n"+\
				"Weg: a distance or a path taken by someone to reach the desired destination\n"+\
				"Your task is to mark the suitable slot within the given utterance, depending on the slot type. Use '**' symbol to mark "+\
				"the begining and the end of the slot. If a suitable slot does not exist, reproduce the given utterance without any markers. "+\
				"Do not give explanations or reasoning. Do not remove any tokens from the sentence.\n"

	test_pred_bio = []

	batch_size = 2
	test_instances = [] # together with examples
	instance_counter = 0
	generated_texts = []

	#for test_utterance, test_id, test_slot, curr_most_sim_ids in zip(test_utterances, test_ids, test_slots, most_sim_ids):
	for test_utterance, test_id, test_slot in zip(test_utterances, test_ids, test_slots):
		if instance_counter == batch_size:
			# process full batch
			outputs = gen_model.chat(test_instances, sampling_params, lora_request=LoRARequest("lora_adapter", 1, "./"+gen_model_name+"-lora-adapter"), use_tqdm=True, chat_template_kwargs={"enable_thinking": False}, add_generation_prompt=True)
			for k, output in enumerate(outputs):
				generated_text = output.outputs[0].text
				generated_texts.append(generated_text)
				#print(f"Generated text: {generated_text!s}")
				#print("-" * 10)
			# reset batch
			instance_counter = 0
			test_instances = []

		test_instance = [{"role": "system", "content": system}, {"role": "user", "content": f"{instruction}\nUtterance: {test_utterance}\nSlot: {test_slot}\nMarked slots:\n"}]
		test_instances.append(test_instance)
		instance_counter += 1

	# in case we still have test instrances:
	if instance_counter != 0:
		outputs = gen_model.chat(test_instances, sampling_params, use_tqdm=True, chat_template_kwargs={"enable_thinking": False}, add_generation_prompt=True)
		for k, output in enumerate(outputs):
			generated_text = output.outputs[0].text
			generated_texts.append(generated_text)
			#print(f"Generated text: {generated_text!s}")
			#print("-" * 10)
		# reset batch
		instance_counter = 0
		test_instances = []

	assert(len(test_gold_bio) == len(generated_texts))
	for p_txt, gold_bio, sl in zip(generated_texts, test_gold_bio, test_slots):
		pred_bio = marked_utterance_to_bio(p_txt)
		# it can happen that a generated sequence contains more (less) tokens than the original one,
		# we trancate / pad it in such cases
		if len(pred_bio) > len(gold_bio):
			pred_bio = pred_bio[:len(gold_bio)]
		else:
			pred_bio += ["O"]*(len(gold_bio)-len(pred_bio))
		test_pred_bio.append(pred_bio)

	# evaluate	
	true_labels = flatten(test_gold_bio)
	predicted_labels = flatten(test_pred_bio)
	print(confusion_matrix(true_labels, predicted_labels))
	print("Micro f1:", f1_score(true_labels, predicted_labels, average="micro"))
	print("Macro f1:", f1_score(true_labels, predicted_labels, average="macro"))
	print("Weighted f1:", f1_score(true_labels, predicted_labels, average="weighted"))
	print()

	# calculate the scores for each slot type separately
	grouped = defaultdict(list)

	for k, p, g in zip(test_slots, test_pred_bio, test_gold_bio):
		grouped[k].append((p, g))

	for k, v in grouped.items():
		print("Slot:", k)
		p, g = zip(*v)
		true_labels = flatten(g)
		predicted_labels = flatten(p)
		print(confusion_matrix(true_labels, predicted_labels))
		print("Micro f1:", f1_score(true_labels, predicted_labels, average="micro"))
		print("Macro f1:", f1_score(true_labels, predicted_labels, average="macro"))
		print("Weighted f1:", f1_score(true_labels, predicted_labels, average="weighted"))
		print()


